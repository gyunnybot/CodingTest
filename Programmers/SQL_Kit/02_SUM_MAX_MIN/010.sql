SELECT YEAR(A.DIFFERENTIATION_DATE) AS YEAR, (B.MAX_SIZE - A.SIZE_OF_COLONY) AS YEAR_DEV, A.ID
FROM ECOLI_DATA AS A LEFT OUTER JOIN (
    SELECT YEAR(DIFFERENTIATION_DATE) AS MAX_YEAR, MAX(SIZE_OF_COLONY) AS MAX_SIZE
    FROM ECOLI_DATA
    GROUP BY MAX_YEAR
) AS B
ON B.MAX_YEAR = YEAR(A.DIFFERENTIATION_DATE)
ORDER BY YEAR ASC, YEAR_DEV ASC;